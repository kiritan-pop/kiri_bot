# メイン
def gen_jinkei():
    DAO = kiri_util.DAO_statuses()
    accts = DAO.get_five()
    if len(accts) != 5:
        return
    jinkei = random.choice(jinkeis)
    j_name = jinkei['name']
    j_mat  = jinkei['mat']
    text = '【' + j_name + '】\n'
    for x in range(5):
        for y in range(5):
            if j_mat[x][y] == 1:
                text += ':@' + accts.pop() + ': '
            else:
                text += bl
        text += '\n'

    return text

if __name__ == '__main__':
    print(gen_jinkei())
